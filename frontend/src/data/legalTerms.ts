// ─── Helpers ──────────────────────────────────────────────────────────────────

/** Find all legal terms that appear in a given text string. */
export function findLegalTermsInText(text: string): string[] {
  return LEGAL_TERMS.filter((lt) => {
    const escaped = lt.term.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    const regex   = new RegExp(`\\b${escaped}\\b`, "i");
    return regex.test(text);
  }).map((lt) => lt.term);
}

/** Look up a single term by exact name (case-insensitive). */
export function getLegalTerm(term: string): LegalTerm | undefined {
  return LEGAL_TERMS.find(
    (lt) => lt.term.toLowerCase() === term.toLowerCase(),
  );
}
